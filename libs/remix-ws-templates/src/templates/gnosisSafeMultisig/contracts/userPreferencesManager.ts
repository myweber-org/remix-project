interface UserPreferences {
  theme: 'light' | 'dark' | 'auto';
  notifications: boolean;
  language: string;
  fontSize: number;
}

class UserPreferencesManager {
  private static readonly STORAGE_KEY = 'user_preferences';
  private static readonly DEFAULT_PREFS: UserPreferences = {
    theme: 'auto',
    notifications: true,
    language: 'en-US',
    fontSize: 14
  };

  private preferences: UserPreferences;

  constructor() {
    this.preferences = this.loadPreferences();
  }

  private loadPreferences(): UserPreferences {
    try {
      const stored = localStorage.getItem(UserPreferencesManager.STORAGE_KEY);
      if (!stored) return { ...UserPreferencesManager.DEFAULT_PREFS };

      const parsed = JSON.parse(stored);
      return this.validatePreferences(parsed);
    } catch {
      return { ...UserPreferencesManager.DEFAULT_PREFS };
    }
  }

  private validatePreferences(data: unknown): UserPreferences {
    const prefs = { ...UserPreferencesManager.DEFAULT_PREFS };

    if (data && typeof data === 'object') {
      const obj = data as Record<string, unknown>;

      if (obj.theme === 'light' || obj.theme === 'dark' || obj.theme === 'auto') {
        prefs.theme = obj.theme;
      }

      if (typeof obj.notifications === 'boolean') {
        prefs.notifications = obj.notifications;
      }

      if (typeof obj.language === 'string' && obj.language.length >= 2) {
        prefs.language = obj.language;
      }

      if (typeof obj.fontSize === 'number' && obj.fontSize >= 8 && obj.fontSize <= 32) {
        prefs.fontSize = obj.fontSize;
      }
    }

    return prefs;
  }

  getPreferences(): UserPreferences {
    return { ...this.preferences };
  }

  updatePreferences(updates: Partial<UserPreferences>): boolean {
    const newPreferences = { ...this.preferences, ...updates };
    
    if (!this.arePreferencesValid(newPreferences)) {
      return false;
    }

    this.preferences = newPreferences;
    this.savePreferences();
    return true;
  }

  private arePreferencesValid(prefs: UserPreferences): boolean {
    return (
      (prefs.theme === 'light' || prefs.theme === 'dark' || prefs.theme === 'auto') &&
      typeof prefs.notifications === 'boolean' &&
      typeof prefs.language === 'string' &&
      prefs.language.length >= 2 &&
      typeof prefs.fontSize === 'number' &&
      prefs.fontSize >= 8 &&
      prefs.fontSize <= 32
    );
  }

  private savePreferences(): void {
    try {
      localStorage.setItem(
        UserPreferencesManager.STORAGE_KEY,
        JSON.stringify(this.preferences)
      );
    } catch (error) {
      console.error('Failed to save preferences:', error);
    }
  }

  resetToDefaults(): void {
    this.preferences = { ...UserPreferencesManager.DEFAULT_PREFS };
    this.savePreferences();
  }

  exportPreferences(): string {
    return JSON.stringify(this.preferences, null, 2);
  }

  importPreferences(jsonString: string): boolean {
    try {
      const parsed = JSON.parse(jsonString);
      const validated = this.validatePreferences(parsed);
      
      if (this.arePreferencesValid(validated)) {
        this.preferences = validated;
        this.savePreferences();
        return true;
      }
      return false;
    } catch {
      return false;
    }
  }
}

export { UserPreferencesManager, type UserPreferences };