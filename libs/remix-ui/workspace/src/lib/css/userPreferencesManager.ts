class UserPreferencesManager {
    private static readonly STORAGE_KEY = 'user_preferences';
    private static readonly DEFAULT_PREFERENCES: UserPreferences = {
        theme: 'auto',
        language: 'en',
        notifications: true,
        fontSize: 14,
        autoSave: true
    };

    private preferences: UserPreferences;

    constructor() {
        this.preferences = this.loadPreferences();
    }

    private loadPreferences(): UserPreferences {
        try {
            const stored = localStorage.getItem(UserPreferencesManager.STORAGE_KEY);
            if (stored) {
                const parsed = JSON.parse(stored);
                return this.validatePreferences(parsed);
            }
        } catch (error) {
            console.warn('Failed to load preferences from storage:', error);
        }
        return { ...UserPreferencesManager.DEFAULT_PREFERENCES };
    }

    private validatePreferences(data: any): UserPreferences {
        const validThemes: Array<UserPreferences['theme']> = ['light', 'dark', 'auto'];
        
        return {
            theme: validThemes.includes(data.theme) ? data.theme : UserPreferencesManager.DEFAULT_PREFERENCES.theme,
            language: typeof data.language === 'string' && data.language.length === 2 
                ? data.language 
                : UserPreferencesManager.DEFAULT_PREFERENCES.language,
            notifications: typeof data.notifications === 'boolean' 
                ? data.notifications 
                : UserPreferencesManager.DEFAULT_PREFERENCES.notifications,
            fontSize: typeof data.fontSize === 'number' && data.fontSize >= 8 && data.fontSize <= 32
                ? data.fontSize
                : UserPreferencesManager.DEFAULT_PREFERENCES.fontSize,
            autoSave: typeof data.autoSave === 'boolean'
                ? data.autoSave
                : UserPreferencesManager.DEFAULT_PREFERENCES.autoSave
        };
    }

    getPreferences(): UserPreferences {
        return { ...this.preferences };
    }

    updatePreferences(updates: Partial<UserPreferences>): void {
        const newPreferences = { ...this.preferences, ...updates };
        this.preferences = this.validatePreferences(newPreferences);
        this.savePreferences();
    }

    private savePreferences(): void {
        try {
            localStorage.setItem(
                UserPreferencesManager.STORAGE_KEY, 
                JSON.stringify(this.preferences)
            );
        } catch (error) {
            console.error('Failed to save preferences:', error);
        }
    }

    resetToDefaults(): void {
        this.preferences = { ...UserPreferencesManager.DEFAULT_PREFERENCES };
        this.savePreferences();
    }

    getTheme(): UserPreferences['theme'] {
        return this.preferences.theme;
    }

    toggleNotifications(): void {
        this.updatePreferences({ notifications: !this.preferences.notifications });
    }

    isAutoSaveEnabled(): boolean {
        return this.preferences.autoSave;
    }
}

