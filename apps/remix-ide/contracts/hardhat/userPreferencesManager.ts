interface UserPreferences {
  theme: 'light' | 'dark' | 'auto';
  language: string;
  notificationsEnabled: boolean;
  itemsPerPage: number;
}

const DEFAULT_PREFERENCES: UserPreferences = {
  theme: 'auto',
  language: 'en-US',
  notificationsEnabled: true,
  itemsPerPage: 25
};

const STORAGE_KEY = 'app_user_preferences';

class UserPreferencesManager {
  private preferences: UserPreferences;

  constructor() {
    this.preferences = this.loadPreferences();
  }

  private loadPreferences(): UserPreferences {
    try {
      const stored = localStorage.getItem(STORAGE_KEY);
      if (stored) {
        const parsed = JSON.parse(stored);
        return { ...DEFAULT_PREFERENCES, ...parsed };
      }
    } catch (error) {
      console.warn('Failed to load user preferences:', error);
    }
    return { ...DEFAULT_PREFERENCES };
  }

  private savePreferences(): void {
    try {
      localStorage.setItem(STORAGE_KEY, JSON.stringify(this.preferences));
    } catch (error) {
      console.warn('Failed to save user preferences:', error);
    }
  }

  getPreferences(): Readonly<UserPreferences> {
    return { ...this.preferences };
  }

  updatePreferences(updates: Partial<UserPreferences>): void {
    this.preferences = { ...this.preferences, ...updates };
    this.savePreferences();
  }

  resetToDefaults(): void {
    this.preferences = { ...DEFAULT_PREFERENCES };
    this.savePreferences();
  }

  getTheme(): UserPreferences['theme'] {
    return this.preferences.theme;
  }

  setTheme(theme: UserPreferences['theme']): void {
    this.updatePreferences({ theme });
  }

  isNotificationsEnabled(): boolean {
    return this.preferences.notificationsEnabled;
  }

  toggleNotifications(): void {
    this.updatePreferences({
      notificationsEnabled: !this.preferences.notificationsEnabled
    });
  }
}

export const userPreferences = new UserPreferencesManager();import { z } from 'zod';

const UserPreferencesSchema = z.object({
  theme: z.enum(['light', 'dark', 'auto']).default('auto'),
  language: z.string().min(2).default('en'),
  notificationsEnabled: z.boolean().default(true),
  itemsPerPage: z.number().min(5).max(100).default(25),
  lastUpdated: z.date().optional()
});

type UserPreferences = z.infer<typeof UserPreferencesSchema>;

class UserPreferencesManager {
  private static readonly STORAGE_KEY = 'user_preferences';
  private preferences: UserPreferences;

  constructor() {
    this.preferences = this.loadPreferences();
  }

  private loadPreferences(): UserPreferences {
    try {
      const stored = localStorage.getItem(UserPreferencesManager.STORAGE_KEY);
      if (stored) {
        const parsed = JSON.parse(stored);
        const result = UserPreferencesSchema.safeParse({
          ...parsed,
          lastUpdated: parsed.lastUpdated ? new Date(parsed.lastUpdated) : undefined
        });
        
      if (result.success) {
        return result.data;
      }
    }
    } catch (error) {
      console.warn('Failed to load preferences:', error);
    }

    return UserPreferencesSchema.parse({});
  }

  private savePreferences(): void {
    try {
      const dataToStore = {
        ...this.preferences,
        lastUpdated: new Date()
      };
      localStorage.setItem(
        UserPreferencesManager.STORAGE_KEY,
        JSON.stringify(dataToStore)
      );
    } catch (error) {
      console.error('Failed to save preferences:', error);
    }
  }

  updatePreferences(updates: Partial<UserPreferences>): UserPreferences {
    const validationResult = UserPreferencesSchema.partial().safeParse(updates);
    
    if (!validationResult.success) {
      throw new Error(`Invalid preferences: ${validationResult.error.message}`);
    }

    this.preferences = {
      ...this.preferences,
      ...validationResult.data
    };
    
    this.savePreferences();
    return this.preferences;
  }

  getPreferences(): UserPreferences {
    return { ...this.preferences };
  }

  resetToDefaults(): UserPreferences {
    this.preferences = UserPreferencesSchema.parse({});
    this.savePreferences();
    return this.preferences;
  }

  exportAsJSON(): string {
    return JSON.stringify(this.preferences, null, 2);
  }

  importFromJSON(json: string): UserPreferences {
    try {
      const parsed = JSON.parse(json);
      return this.updatePreferences(parsed);
    } catch (error) {
      throw new Error('Invalid JSON format for preferences');
    }
  }
}

export { UserPreferencesManager, type UserPreferences };